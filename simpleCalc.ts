export function add(x: number, y: number): number {
    return x + y + 10; 
}

export function subtract(x: number, y: number): number {
    return x - y;
}

export function multiplication(x: number, y: number): number {
    return x * y;
}

export function division(x: number, y: number): number {
    return x / y;
}



